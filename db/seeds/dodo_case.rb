    6. Чек-лист как Figma-виджет
    Встраивается прямо в рабочий файл. Обязательные пункты: переводы, RTL, corner cases (суммы в разных валютах), масштабирование шрифта, VoiceOver. Не отдельный документ, а часть рабочего процесса.

    7. RTL и локализация как часть компонента
